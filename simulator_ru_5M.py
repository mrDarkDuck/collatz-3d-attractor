import sys
import time

# Снимаем встроенный лимит Python на конвертацию длинных строк в числа.
# Устанавливаем запас в 5 000 000 знаков, чтобы миллионник прошел мгновенно.
sys.set_int_max_str_digits(5000000)

def run_siphon_simulation():
    print("=" * 60)
    print(" СИМУЛЯТОР ТОПОЛОГИЧЕСКОГО СИФОНА (ОПТИМИЗИРОВАННЫЙ ВЕРСИЯ v4.0.0)")
    print("=" * 60)
    print("Режим: Сверхглубокое сканирование фазового пространства.")
    print("Ограничения на длину ввода сняты. Построчный вывод отключен для скорости.\n")
    
    # Запрос ввода гигантского числа
    user_input = input("Введите число (хоть на 1 000 000 знаков) и нажмите Enter:\n").strip()
    
    if not user_input.isdigit():
        print("\n[Ошибка]: Ввод должен состоять только из цифр!")
        return

    print("\n[Инициализация]: Конвертируем строку в сверхбольшое число...")
    start_time = time.time()
    
    try:
        # Преобразование строки в bigint
        N = int(user_input)
    except ValueError as e:
        print(f"\n[Ошибка конвертации]: {e}")
        return
        
    print(f"[Успех]: Число успешно загружено в оперативную память.")
    print("[Процесс]: Запуск сифонного сжатия... Пожалуйста, подождите...")
    
    # Инициализация счетчиков инвариантов
    total_steps = 0
    cascade_0a = 0      # Каскады глубокого сжатия (Зеленый)
    rotation_0b = 0     # Пространственные повороты синей оси (Синий)
    growth_pulses = 0   # Импульсы роста
    superparity_traps = 0 # Мгновенные перехваты суперчётностью
    divisions = 0       # Всего шагов деления на 2

    # Основной цикл сифона (расчет в памяти без принтов)
    while N > 1:
        total_steps += 1
        
        # Проверка на чётность (побитовый анализ нижнего бита)
        if N % 2 == 0:
            divisions += 1
            # Логика каскадов 0a и 0b зависит от структуры вашего аттрактора
            # Здесь применяется стандартное распределение траектории:
            if (N // 2) % 2 == 0:
                cascade_0a += 1
            else:
                rotation_0b += 1
            N = N // 2
        else:
            growth_pulses += 1
            # Проверка суперчётности (ловушка Matryoshka of Funnels)
            # Если после шага роста (3N + 1) число мгновенно становится суперчётным (делится на 4)
            next_val = 3 * N + 1
            if next_val % 4 == 0:
                superparity_traps += 1
            N = next_val

    end_time = time.time()
    execution_time = end_time - start_time

    # Расчет эффективности ловушки
    if growth_pulses > 0:
        trap_efficiency = (superparity_traps / growth_pulses) * 100
    else:
        trap_efficiency = 0.0

    # Вывод финальной аналитики
    print("\n" + "=" * 60)
    print(" РЕЗУЛЬТАТЫ СВЕРХГЛУБОКОГО СИФОННОГО СКАНИРОВАНИЯ")
    print("=" * 60)
    print(f"• Общая длина траектории падения: {total_steps} шагов.")
    print(f"• Каскадов глубокого сжатия (Зеленый '0a'): {cascade_0a}")
    print(f"• Пространственных поворотов синей оси (Синий '0b'): {rotation_0b}")
    print(f"• Мгновенных перехватов кривой роста суперчётностью: {superparity_traps} раз.")
    print(f"• Эффективность ловушки суперчётности: {trap_efficiency:.1f}% ваших шагов роста были срезаны мгновенно.")
    print(f"• Баланс сил: {growth_pulses} импульсов роста против {divisions} шагов деления на 2.")
    print(f"• Время чистых вычислений: {execution_time:.2f} сек.")
    print("• Итог: Число достроено до двоичного каркаса и стянуто гравитацией в точку (0,0,0).")
    print("=" * 60)

if __name__ == "__main__":
    run_siphon_simulation()
