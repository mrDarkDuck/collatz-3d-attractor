import sys

def analyze_any_number_3d():
    print("=" * 60)
    print(" ИНТЕРАКТИВНЫЙ 3D-СИМУЛЯТОР РАКОВИНЫ КОЛЛАТЦА")
    print("=" * 60)
    
    # 1. Запрос ввода числа у пользователя
    try:
        user_input = input("Введите любое натуральное число для запуска: ").strip()
        if not user_input:
            print("Вы ничего не ввели.")
            return
        n = int(user_input)
        if n <= 0:
            print("Ошибка: число должно быть строго больше 0.")
            return
    except ValueError:
        print("Ошибка: пожалуйста, введите целое число без букв и пробелов.")
        return

    start_number = n
    print(f"\n[Запуск] Число {start_number} влетает на внешнюю орбиту раковины...\n")
    print(f"{'Шаг':<5} | {'Текущее число':<20} | {'Признак 3D':<15} | {'Траектория в пространстве'}")
    print("-" * 85)

    step = 0
    count_1 = 0
    count_0a = 0
    count_0b = 0
    mgl_captures = 0  # Счётчик мгновенных перехватов суперчётностью

    # Переменная для отслеживания предыдущего шага
    was_growth_last_step = False

    # 2. Главный цикл круговорота признаков
    while n != 1:
        if n % 2 != 0:
            # Нечётный признак 1 (Импульс роста)
            type_str = "1 (Нечетный)"
            action = "💥 Рост 3n+1 -> Импульс вверх"
            count_1 += 1
            next_n = 3 * n + 1
            was_growth_last_step = True
        else:
            # Чётный признак
            if n % 4 == 0:
                type_str = "0a (Супер-чет)"
                # Проверяем, врезалась ли кривая роста сразу в этот желоб
                if was_growth_last_step:
                    action = "⚡ КАПКАН! Рост мгновенно перехвачен супер-чётностью"
                    mgl_captures += 1
                else:
                    action = "🟩 Сжатие -> Скольжение по желобу вниз (Z)"
                count_0a += 1
            else:
                type_str = "0b (Полу-чет)"
                action = "🔵 Нырок по оси Y -> Вираж спирали внутрь"
                count_0b += 1
            
            next_n = n // 2
            was_growth_last_step = False

        # Форматированный вывод каждого шага (красиво выравнивает даже гигантские числа)
        display_num = str(n) if len(str(n)) <= 18 else str(n)[:15] + "..."
        print(f"{step:<5} | {display_num:<20} | {type_str:<15} | {action}")
        
        n = next_n
        step += 1

    # Финальная точка сборки в корне
    print(f"{step:<5} | {1:<20} | 1 (Нечетный)   | 🏁 ДОСТИГНУТ АТТРАКТОР (Центр воронки)")
    count_1 += 1

    # 3. Сухой остаток и проверка вашей теории
    print("=" * 85)
    print(f" СУХОЙ ЛОГИЧЕСКИЙ ОТЧЕТ ДЛЯ ЧИСЛА: {start_number}")
    print("=" * 85)
    print(f"• Общая длина траектории падения: {step} шагов.")
    print(f"• Каскадов глубокого сжатия (Зеленый '0a'): {count_0a}")
    print(f"• Пространственных поворотов синей оси (Синий '0b'): {count_0b}")
    print(f"• Мгновенных перехватов кривой роста суперчётностью: {mgl_captures} раз.")
    
    if count_1 > 1:
        capture_rate = (mgl_captures / (count_1 - 1)) * 100
        print(f"• Эффективность ловушки суперчётности: {capture_rate:.1f}% ваших шагов роста были срезаны мгновенно.")
    
    total_deletions = count_0a + count_0b
    print(f"• Баланс сил: {count_1} импульсов роста против {total_deletions} шагов деления на 2.")
    
    # Главный вывод на основе степеней двойки
    print(f"• Итог: Число достроено до двоичного каркаса и стянуто гравитацией в точку (0,0,0).")
    print("=" * 85)

# Запуск интерактивной программы
if __name__ == "__main__":
    analyze_any_number_3d()
